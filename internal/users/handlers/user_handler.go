func isValidUserStatus(status users.UserStatus) bool {
	switch status {
	case users.UserStatusActive,
		users.UserStatusInactive,
		users.UserStatusSuspended,
		users.UserStatusDeleted:
		return true
	default:
		return false
	}
}
